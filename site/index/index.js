



document.getElementById("b2").addEventListener("mouseover", function(){
    document.getElementById("fond2").classList.replace("blue", "aqua");
    document.getElementById("b2").classList.replace("blue", "aqua");
    document.getElementById("proj").classList.replace("logo_proj2", "logo_proj1");
})
document.getElementById("b2").addEventListener("mouseout", function(){
  document.getElementById("fond2").classList.replace("aqua", "blue");
  document.getElementById("b2").classList.replace("aqua", "blue");
  document.getElementById("proj").classList.replace("logo_proj1", "logo_proj2");
})



